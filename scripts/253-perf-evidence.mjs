#!/usr/bin/env node
/**
 * @file scripts/253-perf-evidence.mjs
 * @description Perf-evidence harness for Issue #253. Builds the site, serves
 * dist/ on an isolated preview port, runs the FPS E2E spec against it on both
 * desktop + mobile device profiles, and saves the JSON+screenshots to the
 * evidence dir.
 *
 * Why a script wraps the spec: the perf-honesty gate (iter-16) requires every
 * cited perf number to trace to a saved JSON whose device field matches the
 * claim. The script guarantees (a) the server is the BUILT preview (not a stale
 * dev server a concurrent lane left running on :3000), (b) the device profile
 * is set explicitly via PERF_DEVICE so the spec's skip-gates fire correctly,
 * and (c) the evidence files land in the ticket's evidence dir.
 *
 * Usage:
 *   node scripts/253-perf-evidence.mjs
 *
 * @ticket #253
 */
import { spawn, spawnSync } from 'node:child_process'
import { mkdirSync, existsSync, readdirSync, writeFileSync, unlinkSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')
const PORT = '4173'
const EVIDENCE_DIR = resolve(
  ROOT,
  'projects/kttech-cyber/projects/253/evidence',
)
// npm env quirk in this workspace: the shared node_modules lives behind a
// symlink and the system npm needs an isolated cache dir. Default to the
// absolute npm binary + cache dir; allow override via NPM_BIN.
const NPM = process.env.NPM_BIN || '/usr/local/bin/npm'
const NPM_CACHE = process.env.NPM_CACHE || '/tmp/npm-cache-ktech'

function log(msg) {
  console.log(`[253-evidence] ${msg}`)
}

function run(cmd, args, opts = {}) {
  // Use shell:true so a cmd with embedded flags (e.g. npm --cache X) resolves,
  // and propagate the npm cache + root env so child npm/playwright inherit it.
  const env = {
    ...process.env,
    npm_config_cache: NPM_CACHE,
    ...(opts.env || {}),
  }
  const result = spawnSync([cmd, ...args].join(' '), [], {
    stdio: 'inherit',
    cwd: ROOT,
    shell: true,
    ...opts,
    env,
  })
  if (result.status !== 0) {
    throw new Error(`${cmd} ${args.join(' ')} exited ${result.status}`)
  }
  return result
}

async function waitForServer(url, timeoutMs = 30000) {
  const start = Date.now()
  while (Date.now() - start < timeoutMs) {
    try {
      const res = await fetch(url)
      if (res.ok || res.status === 404) return true
    } catch {
      // not ready
    }
    await new Promise((r) => setTimeout(r, 500))
  }
  throw new Error(`server at ${url} did not become ready in ${timeoutMs}ms`)
}

async function main() {
  mkdirSync(EVIDENCE_DIR, { recursive: true })

  log('Building production bundle...')
  run(NPM, ['run', 'build'])

  log(`Starting preview server on :${PORT}...`)
  const server = spawn(
    `${NPM} run preview -- --port ${PORT} --strictPort`,
    [],
    {
      stdio: 'pipe',
      cwd: ROOT,
      shell: true,
      env: { ...process.env, npm_config_cache: NPM_CACHE },
    },
  )
  server.stdout?.on('data', (d) => process.stderr.write(d))
  server.stderr?.on('data', (d) => process.stderr.write(d))

  try {
    await waitForServer(`http://localhost:${PORT}/KTechAICyberWeb/`)
    log('Preview server is up.')

    const PW = resolve(ROOT, 'node_modules/.bin/playwright')

    // Overlay config: disable Playwright's own webServer (it would try
    // `npm run dev` on :3000 — wrong server + broken PATH npm in this
    // workspace). The spec targets PERF_PORT directly against our preview
    // server, so Playwright does NOT need to start one.
    const overlayPath = resolve(ROOT, 'playwright.perf-overlay.mjs')
    writeFileSync(
      overlayPath,
      `// Temporary overlay generated by scripts/253-perf-evidence.mjs — do NOT commit.\n` +
        `// Disables webServer (the harness manages the preview server on PERF_PORT).\n` +
        `import base from './playwright.config.ts'\n` +
        `export default { ...base, webServer: undefined }\n`,
    )

    try {
      log('Running desktop FPS gate (chromium)...')
      run(
        PW,
        [
          'test',
          'tests/e2e/253-perf-fps.spec.ts',
          `--config=${overlayPath}`,
          '--project=chromium',
          '--reporter=list',
        ],
        {
          env: {
            ...process.env,
            PERF_PORT: PORT,
            PERF_DEVICE: 'desktop',
            PERF_EVIDENCE_DIR: EVIDENCE_DIR,
          },
        },
      )

      log('Running mobile FPS gate (Mobile Chrome)...')
      run(
        PW,
        [
          'test',
          'tests/e2e/253-perf-fps.spec.ts',
          `--config=${overlayPath}`,
          // Quote the project name so the shell does not split "Mobile Chrome".
          '--project=Mobile\\ Chrome',
          '--reporter=list',
        ],
        {
          env: {
            ...process.env,
            PERF_PORT: PORT,
            PERF_DEVICE: 'mobile',
            PERF_EVIDENCE_DIR: EVIDENCE_DIR,
          },
        },
      )
    } finally {
      // Clean up the overlay so it never gets committed.
      try {
        unlinkSync(overlayPath)
      } catch {
        /* already gone */
      }
    }

    log('Evidence files:')
    for (const f of readdirSync(EVIDENCE_DIR)) {
      log(`  ${f}`)
    }
    log('Done.')
  } finally {
    log('Stopping preview server...')
    server.kill('SIGTERM')
  }
}

main().catch((err) => {
  console.error('[253-evidence] FAILED:', err)
  process.exit(1)
})
